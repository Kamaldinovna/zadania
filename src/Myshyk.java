public class Myshyk {
    private String myshykaty;
    private String myshyktusu;
    private String maalymat;

    public void setMyshykaty (String myshykaty1){
        myshykaty = myshykaty1;
    }
    public void setMyshyktusu (String myshyktusu1){
        myshyktusu = myshyktusu1;
    }
    public void setMaalymat (String maalymat1){
        maalymat = maalymat1;
    }


    public String getMyshykaty (){
        return myshykaty;
    }
    public String getMyshyktusu(){
        return myshyktusu;
    }
    public String getMaalymat (){
        return maalymat;
    }
}
